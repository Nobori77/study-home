console.log(10); // 숫자 리터럴

console.log(3.14); // 넘버 타입

// 자바스크립트는 숫자타입을 구분안함

console.log(0b0000010); // 0b = 2진수 값은 = 2

console.log(0o111); // 0o = 8진수 값은 73

console.log(0xff); // 0x = 헥사 16진수 

// 타입은 다 숫자

console.log('hello') // 문자열 타입 string

console.log (false); // 값은 false 타입은 불리언

console.log (null); // null 은 값이자 타입 

console.log(undefined); // undefinde 값이자 타입

console.log({'name' : '홍길동', 'age': 20}); // 개체 name , age 키 홍길동 ,20 벨류 복합타입

console.log([1,2,3]); // 배열 여러가지 값을 하나의 변수로 저장하기 위한 값

console.log(function f(){})

console.log(/[a-z]/); // 정규 표현식

